package sef.finalactivity;

public class Calculator {
	public double mult(double a, double b) {
		return a * b;
	}

	public double div(double a, double b) {
		return a / b;
	}

	public double sum(double a, double b) {
		return a + b;
	}

	public double diff(double a, double b) {
		return a - b;
	}
	
}
